def soma(a, b, c) -> int or float:
        somatorio = a + b + c
        return somatorio


def mult(a, b, c) -> int or float:
        produto = a * b * c
        return produto


def verifica_palindromo(texto) -> bool:
        if texto == texto[::-1]:
            return True
        else:
            return False


def divisao(a, b) -> int or float:
    return a / b


